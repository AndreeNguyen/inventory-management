package view.Tongquan;

/**
 *
 * @author doqua
 */
public class kho {
    int dtkho,dtchua,dtdgdi,dtsd;

    public kho() {
    }

    public kho(int dtkho, int dtchua, int dtdgdi, int dtsd) {
        this.dtkho = dtkho;
        this.dtchua = dtchua;
        this.dtdgdi = dtdgdi;
        this.dtsd = dtsd;
    }

    public int getDtkho() {
        return dtkho;
    }

    public void setDtkho(int dtkho) {
        this.dtkho = dtkho;
    }

    public int getDtchua() {
        return dtchua;
    }

    public void setDtchua(int dtchua) {
        this.dtchua = dtchua;
    }

    public int getDtdgdi() {
        return dtdgdi;
    }

    public void setDtdgdi(int dtdgdi) {
        this.dtdgdi = dtdgdi;
    }

    public int getDtsd() {
        return dtsd;
    }

    public void setDtsd(int dtsd) {
        this.dtsd = dtsd;
    }
    
}
